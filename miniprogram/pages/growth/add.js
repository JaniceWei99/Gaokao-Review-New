var app = getApp();
var storage = require('../../services/storage');
var imageUtil = require('../../utils/image');
var uploadService = require('../../services/upload');
var growthService = require('../../services/growth');

var RECORD_TYPES = [
  { key: 'award', label: '获奖', icon: '🏆' },
  { key: 'progress', label: '进步', icon: '📈' },
  { key: 'performance', label: '演出', icon: '🎭' },
  { key: 'breakthrough', label: '突破', icon: '💡' },
  { key: 'memo', label: '备忘', icon: '📝' }
];

Page({
  data: {
    recordTypes: RECORD_TYPES,
    selectedType: '',
    title: '',
    description: '',
    recordDate: '',
    category: '',
    awardingBody: '',
    imagePath: '',
    matchedQuote: null,
    submitting: false
  },

  onLoad: function(options) {
    var today = new Date();
    var dateStr = today.getFullYear() + '-' +
      String(today.getMonth() + 1).padStart(2, '0') + '-' +
      String(today.getDate()).padStart(2, '0');

    var data = { recordDate: dateStr };

    if (options) {
      if (options.record_type) data.selectedType = decodeURIComponent(options.record_type);
      if (options.title) data.title = decodeURIComponent(options.title);
      if (options.description) data.description = decodeURIComponent(options.description);
      if (options.auto_generated === '1') data.autoGenerated = true;
    }

    this.setData(data);
  },

  onSelectType: function(e) {
    var type = e.currentTarget.dataset.key;
    this.setData({ selectedType: type });
  },

  onInputTitle: function(e) {
    this.setData({ title: e.detail.value });
  },

  onInputDescription: function(e) {
    this.setData({ description: e.detail.value });
  },

  onDateChange: function(e) {
    this.setData({ recordDate: e.detail.value });
  },

  onInputCategory: function(e) {
    this.setData({ category: e.detail.value });
  },

  onInputAwardingBody: function(e) {
    this.setData({ awardingBody: e.detail.value });
  },

  onChooseImage: function() {
    var that = this;
    imageUtil.compressAndChoose(1).then(function(paths) {
      if (paths.length > 0) {
        that.setData({ imagePath: paths[0] });
      }
    });
  },

  onRemoveImage: function() {
    this.setData({ imagePath: '' });
  },

  onSubmit: function() {
    var that = this;
    if (that.data.submitting) return;

    if (!that.data.selectedType) {
      wx.showToast({ title: '请选择记录类型', icon: 'none' });
      return;
    }
    if (!that.data.title.trim()) {
      wx.showToast({ title: '请输入标题', icon: 'none' });
      return;
    }

    var isLoggedIn = app.isLoggedIn();
    var studentId = app.getCurrentStudentId();

    that.setData({ submitting: true });

    if (!isLoggedIn || !studentId) {
      var record = {
        id: 'local_' + Date.now(),
        record_type: that.data.selectedType,
        title: that.data.title.trim(),
        description: that.data.description || null,
        record_date: that.data.recordDate,
        category: that.data.category || null,
        awarding_body: that.data.awardingBody || null,
        image_url: that.data.imagePath,
        auto_generated: that.data.autoGenerated || false,
        _localCreated: Date.now()
      };
      storage.saveGrowthRecord(record);
      wx.showToast({ title: '已保存到本地', icon: 'success' });
      setTimeout(function() { wx.navigateBack(); }, 1000);
      return;
    }

    var uploadPromise = that.data.imagePath
      ? that._uploadImage(that.data.imagePath, studentId)
      : Promise.resolve(null);

    uploadPromise.then(function(imageUrl) {
      var payload = {
        record_type: that.data.selectedType,
        title: that.data.title.trim(),
        description: that.data.description || null,
        record_date: that.data.recordDate,
        category: that.data.category || null,
        awarding_body: that.data.awardingBody || null,
        image_url: imageUrl,
        auto_generated: that.data.autoGenerated || false
      };

      return growthService.createGrowthRecord(studentId, payload);
    }).then(function(res) {
      wx.showToast({ title: '保存成功', icon: 'success' });
      setTimeout(function() { wx.navigateBack(); }, 1000);
    }).catch(function(err) {
      console.warn('[GrowthAdd] submit error:', err);
      that.setData({ submitting: false });
    });
  },

  _uploadImage: function(filePath, studentId) {
    return uploadService.requestUploadCredential('growth', studentId).then(function(creds) {
      return uploadService.uploadImageToCOS(filePath, creds).then(function() {
        return creds.image_key;
      });
    });
  }
});